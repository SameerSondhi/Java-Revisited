public class NumbersOverview {
    public static void main(String[] args) {
        byte myByte = 13;
        boolean myFlag = true;
        short myShort = 45;
        char myChar = 'g';
        int myInt = 10;
        char biggerShort = 65000;

        // Floating point numbers
        // Float and double
        // Must add F or f at the end of float to indicate that it is a float and not a double
        float myFloat = 3.141592f;
        double myDouble = 3.141592;
        System.out.println(myByte);

        // Hexadecimal values (need to use 'x')
        byte hexByte = 0x1f;

        // Binary values (need to use 'b')
        int binary = 0b10101;




    }
}
