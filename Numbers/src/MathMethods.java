public class MathMethods {
    public static void main(String[] args) {
        // Absolute value
        System.out.println(Math.abs(-56)); // Prints 56

        System.out.println(Math.ceil(5.01));
        System.out.println(Math.floor(6.7));

        System.out.println(Math.min(5,7));
        System.out.println(Math.max(5,7));

        System.out.println(Math.pow(2,3)); // Returns a double

        System.out.println((int)(Math.random() * 10));

        System.out.println(Math.round(5.5));
        System.out.println(Math.sqrt(64));

        System.out.println(Math.PI); // PI
        System.out.println(Math.E); // Euler



    }
}
